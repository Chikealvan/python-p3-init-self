#!/usr/bin/env python3

# Define a class called 'Person'
class Person: 
    # Constructor method (__init__) for initializing instances of the class
    def __init__(self, name):
        self.name = name

# Create a Person instance with the name "Chike Alvan"
person = Person("Chike Alvan")

# Access the person's name
print(person.name) 

